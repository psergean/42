#include "libft.h"
#include <stdlib.h>

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list *list;
	t_list *tmp;

	list = *alst;
	while (list)
	{
		tmp = list;
		del(list->content, list->content_size);
		free(list);
		list = tmp->next;
	}
	*alst = NULL;
}
