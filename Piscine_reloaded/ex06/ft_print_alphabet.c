void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int index;

	index = 0;
	while (str[index] != '\0')
	{
		ft_putchar(str[index]);
		index++;
	}
}

void	ft_print_alphabet(void)
{
	ft_putstr("abcdefghijklmnopqrstuvwxyz");
}
