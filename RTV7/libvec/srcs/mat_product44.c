#include "libvec.h"

double	**mat_product44(double **m1, double **m2)
{
	int		i;
	int		j;
	double	**m3;

	i = -1;
	m3 = mat_create44();
	while (++i < 4)
	{
		j = -1;
		while (++j < 4)
		{
			m3[i][j] = m1[i][0] * m2[0][j] + m1[i][1] * m2[1][j] +
				m1[i][2] * m2[2][j] + m1[i][3] * m2[3][j];
		}
	}
	return (m3);
}
